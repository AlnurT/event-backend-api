"""
Модуль содержит класс DBManager — асинхронный контекстный менеджер для управления
сессией базы данных (session).
"""


class DBManager:
    """
    Асинхронный контекстный менеджер для работы с сессией БД.

     Экземпляр класса:
    - session_factory: фабрика для создания сессий, поддерживает асинхронные вызовы.

     Поведение:
    - при входе в менеджер создаётся сессия и возвращается менеджер.
    - при выходе из менеджера всегда выполняется rollback и close
      для корректного завершения сессии в случае исключений.
    - для сохранения транзакции нужно вызвать commit внутри контекста.
    """

    def __init__(self, session_factory):
        """
        Инициализация контекстного менеджера.

        :param session_factory: фабрика сессий.
        """
        self.session_factory = session_factory

    async def __aenter__(self):
        """
        Создаёт сессию через session_factory и возвращает менеджер.

        :return: self — у объекта будет атрибут session, содержащий созданную сессию.
        """
        # Создаём сессию в момент входа в контекст
        self.session = self.session_factory()
        return self

    async def __aexit__(self, *args):
        """
        Вызывается при выходе из асинхронного контекста.

         Поведение:
        - выполняет откат транзакции rollback (без проверки ошибок),
          чтобы не оставлять незавершённую транзакцию.
        - закрывает сессию вызовом close.
        """
        await self.session.rollback()
        await self.session.close()

    async def commit(self):
        """
        Подтверждается текущая транзакция сессии.
        """
        await self.session.commit()
